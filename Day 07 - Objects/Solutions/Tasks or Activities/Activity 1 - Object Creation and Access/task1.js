/**
 * Day 7 - Objects
 *
 * Activity 1: Object Creation and Access
 *  - Task 1: Create a book object with properties like title, author, and year. Log it to the console.
 */



// creating book object with properties like title, author, and year
let book = {
    title: "JavaScript",
    author: "Bharat",
    year: 2021
};

// printing the book object
console.log(book);

// Access and printing the title, author and year properties
console.log("Title: " + book.title);
console.log("Author: " + book.author);
console.log("Year: " + book.year);