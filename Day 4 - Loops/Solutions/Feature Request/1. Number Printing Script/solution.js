/**
 * Day 4 - Loops
 * 
 * Feature Request: Number Printing Script
 *  - Write a script that prints numbers from 1 to 10 using a for loop and a while loop.
 */


// Using a for loop
console.log('Using a for loop:');

for (let i = 1; i <= 10; i++) {
    console.log(i);
}


// Using a while loop
console.log('\nUsing a while loop:');

let i = 1;

while (i <= 10) {
    console.log(i);
    i++;
}
